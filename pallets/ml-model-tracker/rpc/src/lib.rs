//! RPC interface for the transaction payment module.

use jsonrpc_core::{Error as RpcError, ErrorCode, Result};
use jsonrpc_derive::rpc;
use sp_api::ProvideRuntimeApi;
use sp_blockchain::HeaderBackend;
use sp_runtime::{generic::BlockId, traits::Block as BlockT};
use std::sync::Arc;
use ml_model_tracker_runtime_api::MlModelTrackerApi as MlModelTrackerRuntimeApi;

#[rpc]
pub trait MlModelTrackerApi<BlockHash> {
	#[rpc(name = "mlModelTracker_getAggregateModel")]
	fn get_aggregate_model(&self, at: Option<BlockHash>) -> Result<Vec<u32>>;
}

/// A struct that implements the `SumStorageApi`.
pub struct MlModelTracker<C, M> {
	// If you have more generics, no need to SumStorage<C, M, N, P, ...>
	// just use a tuple like SumStorage<C, (M, N, P, ...)>
	client: Arc<C>,
	_marker: std::marker::PhantomData<M>,
}

impl<C, M> MlModelTracker<C, M> {
	/// Create new `SumStorage` instance with the given reference to the client.
	pub fn new(client: Arc<C>) -> Self {
		Self {
			client,
			_marker: Default::default(),
		}
	}
}

impl<C, Block> MlModelTrackerApi<<Block as BlockT>::Hash> for MlModelTracker<C, Block>
where
	Block: BlockT,
	C: Send + Sync + 'static,
	C: ProvideRuntimeApi<Block>,
	C: HeaderBackend<Block>,
	C::Api: MlModelTrackerRuntimeApi<Block>,
{
	fn get_aggregate_model(&self, at: Option<<Block as BlockT>::Hash>) -> Result<Vec<u32>> {
		let api = self.client.runtime_api();
		let at = BlockId::hash(at.unwrap_or_else(||
			// If the block hash is not supplied assume the best block.
			self.client.info().best_hash));

		let runtime_api_result = api.get_aggregate_model(&at);
		runtime_api_result.map_err(|e| RpcError {
			code: ErrorCode::ServerError(9876), // No real reason for this value
			message: "Something wrong".into(),
			data: Some(format!("{:?}", e).into()),
		})
	}
}
